                                        <h4>Ciclos de vida</h4>
                                        <p class = "textabout">Las moscas domésticas viven hasta 3 semanas y son activas durante la parte más cálida del año. Una hembra adulta deposita de 50 a 100 huevos de una vez y alrededor de 500 huevos durante su vida. Los huevos se depositan en materia orgánica en estado de putrefacción. Incuban luego de 12 horas y las larvas o gusanos se alimentan de materiales orgánicos en estado de putrefacción.</p>
                                        <p class = "textabout">Un gusano pasa por tres estados larvales y luego forma una crisálida o pupa. La mosca adulta emerge de la pupa. Las moscas domésticas por lo general se desarrollan desde un huevo al adulto dentro de los 10 días.El control de moscas puede ser aplicado contra adultos o larvas. La buena higiene es el paso básico en todo manejo de moscas.</p>
