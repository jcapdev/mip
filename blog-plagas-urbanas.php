                                <h4>MOSCAS:</h4>
                                <p class = "textabout">Lamosca doméstica puede encontrarse en todo el mundo y vive cerca de los humanos. Dado que las moscas domésticas son portadoras de muchas enfermedades en personas y animales domésticos, es importante mantener baja la población de estos insectos. Entender sus hábitos y su ciclo de vida puede ayudar a eliminar gran parte de las moscas a bajo costo para usted y medioambiente.</p>
                                <div class = "row">                                   
                                    <div class="col-md-6">                                        
                                        <p class = "textabout"> mosca doméstica es una mosca que no pica y que mide alrededor de 1⁄4 pulgada de largo. Las moscas domésticas adultas tienen dos alas y cuatro bandas negras en sentido vertical en la parte posterior. Por lo general, el abdomen parece ser a cuadros.</p>
                                        <p class = "textabout">Las moscas domésticas, por lo general, se confunden con las moscardas de la carne y las moscas de los establos. Las moscardas de la carne sólo tienen tres bandas en su parte posterior y la punta del abdomen es generalmente roja. La mosca de los establos se alimenta de sangre y las partes de su boca sobresalen de la parte frontal de su cabeza.</p>
