                                <p class = "textabout">La hormiga es un pequeño insecto himenóptero de los trópicos y zonas templadas que corresponde a unas 3,500 especies de la familia formícidos.Todas las hormigas conocidas son sociables; viven en colonias, que pueden estar compuestas por unos pocos individuos.</p>
